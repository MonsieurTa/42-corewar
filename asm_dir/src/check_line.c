int		check_line(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] != ' ' || str[i] != '\t' || str[i] != '\n' || str[i] != '#')
			return (1);
		i++;
	}
	return (0);
}
